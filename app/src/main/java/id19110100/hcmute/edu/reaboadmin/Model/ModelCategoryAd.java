package id19110100.hcmute.edu.reaboadmin.Model;

public class ModelCategoryAd {

    String id, category, uid;
    long timestamp;

    //constructor empty required for firebase
    public ModelCategoryAd(){

    }
    //constructor

    public ModelCategoryAd(String id, String category, String uid, long timestamp) {
        this.id = id;
        this.category = category;
        this.uid = uid;
        this.timestamp = timestamp;
    }

    //get--set

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }
}
